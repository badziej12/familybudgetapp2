function showChartDropdown(chartDropdown, monthDropdown) {
    if (chartDropdown.style.display == 'block' || monthDropdown.style.display == 'block') {
        chartDropdown.style.display = 'none';
        monthDropdown.style.display = 'none';
    } else {
        chartDropdown.style.display = 'block';
    }
}






